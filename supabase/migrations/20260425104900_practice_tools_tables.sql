-- Translation Table
CREATE TABLE practice_translations (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    mode TEXT NOT NULL CHECK (mode IN ('ar-to-bn', 'bn-to-ar')),
    ar_text TEXT NOT NULL,
    bn_text TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- Tashkil and Correction Table
CREATE TABLE practice_tashkils (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    type TEXT NOT NULL CHECK (type IN ('correction', 'harakat')),
    wrong_text TEXT, -- For correction
    correct_text TEXT NOT NULL, -- The correct sentence (or with harakat)
    rule_text TEXT, -- For correction
    no_haraka_text TEXT, -- For harakat
    meaning TEXT, -- For harakat
    iraab_json JSONB, -- Array of {word, role, haraka} for harakat
    created_at TIMESTAMPTZ DEFAULT now()
);

-- Insha Table
CREATE TABLE practice_inshas (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    type TEXT NOT NULL CHECK (type IN ('application', 'letter', 'essay')),
    title TEXT,
    ar_text TEXT NOT NULL,
    bn_text TEXT,
    key_phrases_json JSONB, -- Array of {ar, bn}
    created_at TIMESTAMPTZ DEFAULT now()
);

-- Enable RLS
ALTER TABLE practice_translations ENABLE ROW LEVEL SECURITY;
ALTER TABLE practice_tashkils ENABLE ROW LEVEL SECURITY;
ALTER TABLE practice_inshas ENABLE ROW LEVEL SECURITY;

-- Policies for Translations
CREATE POLICY "Translations are viewable by everyone" ON practice_translations FOR SELECT USING (true);
CREATE POLICY "Admins can manage translations" ON practice_translations USING (
    EXISTS (SELECT 1 FROM user_roles WHERE user_roles.user_id = auth.uid() AND user_roles.role = 'admin')
);

-- Policies for Tashkils
CREATE POLICY "Tashkils are viewable by everyone" ON practice_tashkils FOR SELECT USING (true);
CREATE POLICY "Admins can manage tashkils" ON practice_tashkils USING (
    EXISTS (SELECT 1 FROM user_roles WHERE user_roles.user_id = auth.uid() AND user_roles.role = 'admin')
);

-- Policies for Inshas
CREATE POLICY "Inshas are viewable by everyone" ON practice_inshas FOR SELECT USING (true);
CREATE POLICY "Admins can manage inshas" ON practice_inshas USING (
    EXISTS (SELECT 1 FROM user_roles WHERE user_roles.user_id = auth.uid() AND user_roles.role = 'admin')
);

